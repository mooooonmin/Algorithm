public class Main{
	
	public static void main(String args[]){
        // 첫 번째 라인: |\ (백슬래시) _ / (백슬래시) |
		System.out.println("|\\_/|");
        // 두 번째 라인: | q p |   } / (백슬래시)
		System.out.println("|q p|   /}");
        // 세 번째 라인: ( 0 ) """ (백슬래시)
		System.out.println("( 0 )\"\"\"\\");
        // 네 번째 라인: | " ^ " `    |
		System.out.println("|\"^\"`    |");
        // 다섯 번째 라인: | | (백슬래시) _ / = \ (백슬래시) \ (백슬래시) | _ |
		System.out.println("||_/=\\\\__|");
	}
	
}